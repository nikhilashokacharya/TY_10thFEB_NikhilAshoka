package com.tyss.capgemini.operators;

public class RelationalOperators 
{

	public static void main(String[] args) 
	{
		int i = 97;
		int j = 45;

		System.out.println("i less than j: " + (i < j)); // lesser than
		System.out.println("i greater than j: " + (i > j)); // greater than
		System.out.println("i less than or equal to j: " + (i <= j)); // lesser than or equal
		System.out.println("i greater than or equal to j: " + (i >= j)); // greater than or equal
		System.out.println("i is equal to j: " + (i == j)); // is equal
		System.out.println("i is not equal to j: " + (i != j)); // is not equal
	}

}
